package sample;

public class Specialite {
    //Variables
    private int id;
    private String nom;

    //Constructor

    public Specialite(int id, String nom) {
        this.id = id;
        this.nom = nom;
    }


    //Getters/Setters

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNom() {
        return nom;
    }

    public void setNom(String nom) {
        this.nom = nom;
    }


    //Methodes




}
